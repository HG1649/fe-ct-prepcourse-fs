
  // La función recibe dos argumentos llamados "x" e "y" que son números.
  // Retorna el resultado de la suma de estos argumentos.
  // Por ejemplo: 
  // 5, 5 ---> 10
  // -5, 5 ---> 0
  // Tu código:
  
  function suma(x, y) {
    // Se realiza la suma de los argumentos 'x' e 'y'
    const resultado = x + y;
  
    // Se retorna el valor de la suma
    return resultado;
  }
  
  module.exports = suma;


