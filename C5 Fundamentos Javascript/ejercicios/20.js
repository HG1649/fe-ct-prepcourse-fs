
  // La función recibe dos argumentos llamados "x" e "y" que son números.
  // Retorna el resultado de la resta de estos argumentos.
  // Por ejemplo: 
  // 10, 5 ---> 5
  // 5, 5 ---> 0
  // Tu código:

  function resta(x, y) {
    // Se realiza la resta de los argumentos 'x' e 'y'
    const resultado = x - y;
  
    // Se retorna el valor de la resta
    return resultado;
  }
  
  module.exports = resta;
